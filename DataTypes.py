#List
# Definition: A list is a collection of items that are ordered and mutable (changeable). Lists are denoted by square brackets [ ].

# Define a list of integers
numbers = [1, 2, 3, 4, 5]
print("numbers:", numbers)

# Define a list of strings
fruits = ["apple", "banana", "orange"]
print("fruits:", fruits)

# Define a list of mixed data types
mixed = [1, "apple", True, 3.14]
print("mixed:", mixed)


#Dictionary:
#Definition: A dictionary is a collection of key-value pairs that are unordered and mutable.
#Dictionaries are denoted by curly braces { }, and each key-value pair is separated by a colon :.

# Define a dictionary of student grades
