#Tuple:
#Definition: A tuple is an ordered and immutable collection of items. Tuples are denoted by parentheses ( ).

# Define a tuple of coordinates
point = (10, 20)
print("point:", point)

# Define a tuple of mixed data types
info = ("John", 25, "New York")
print("info:", info)


#Set:
#Definition: A set is an unordered collection of unique items. Sets are denoted by curly braces { }.

# Define a set of unique numbers
numbers_set = {1, 2, 3, 4, 5}
print("numbers_set:", numbers_set)

# Define a set of unique characters
letters_set = {"a", "b", "c", "d"}
print("letters_set:", letters_set)






